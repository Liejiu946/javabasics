package com.learn.grammar;

public class grammar_01 {
        /*基础语法
        * 1.基本数据类型
        * 分为：整型、浮点型、字符、布尔
        * String不属于基本数据类型*/

        /*整型*/
        byte aByte = 0;
        short aShort = 0;
        int anInt = 0;
        long aLong = 1L;
        /*浮点型*/
        float aFloat = 0;
        double aDouble = 0.1;
        /*字符型*/
        char aChar = 'a';
        /*布尔*/
        boolean aBoolean = false;
        /*String是一个类*/
        String string = "abc";

        /*基本数据类型的分装类*/
        Byte aByte2 = 0;
}
